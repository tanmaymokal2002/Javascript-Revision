let age = 25; // number
let myName = "Alice"; // string
let isStudent = true; // boolean
let myStatus = null; // null
let city; // undefined

// Symbols are less commonly used in everyday code
const idSymbol = Symbol("id");

console.log(typeof age); // Output: "number"
console.log(typeof myName); // Output: "string"
console.log(typeof isStudent); // Output: "boolean"
console.log(typeof myStatus); // Output: "object" (null is a special case)
console.log(typeof city); // Output: "undefined"
console.log(typeof idSymbol); // Output: "symbol"
